class IMPORT_OT_heightmap(bpy.types.Operator):
    """Import a heightmap PNG as terrain mesh for OPCD course building"""
    bl_idname = "import_scene.heightmap_dem"
    bl_label = "Import Heightmap DEM"
    bl_options = {'REGISTER', 'UNDO'}

    # File browser properties
    filepath: bpy.props.StringProperty(
        subtype='FILE_PATH',
        default="",
    )

    filter_glob: bpy.props.StringProperty(
        default="*.png;*.tif;*.tiff",
        options={'HIDDEN'},
    )

    # Option: use vertex displacement instead of modifier
    use_vertex_displacement: bpy.props.BoolProperty(
        name="Direct Vertex Displacement",
        description="Modify vertices directly instead of using a modifier. "
                    "More reliable for export but slower to apply",
        default=False,
    )

    def execute(self, context):
        if not self.filepath:
            self.report({'ERROR'}, "No file selected")
            return {'CANCELLED'}

        print(f"\n{'='*60}")
        print(f"  IMPORTING HEIGHTMAP: {self.filepath}")
        print(f"{'='*60}\n")

        # Step 1: Load metadata
        meta = load_metadata(self.filepath)
        print(f"[import_dem] Real dimensions: {meta['real_width_m']:.0f}m x {meta['real_height_m']:.0f}m")
        print(f"[import_dem] Elevation: {meta['elevation_min']:.1f}m - {meta['elevation_max']:.1f}m")

        # Step 2: Clean up any existing terrain
        cleanup_existing()

        # Step 3: Load the heightmap image
        heightmap_img = load_heightmap_image(self.filepath)
        img_w, img_h = heightmap_img.size

        # Step 4: Calculate subdivision level
        subdiv_level = calculate_subdivisions(img_w, img_h)

        # Step 5: Create the terrain plane
        terrain = create_terrain_plane(meta)

        # Step 6: Subdivide the terrain
        subdivide_terrain(terrain, subdiv_level)

        # Step 7: Apply displacement
        if self.use_vertex_displacement:
            apply_displacement_via_vertices(terrain, heightmap_img, meta)
        else:
            apply_displacement_texture(terrain, heightmap_img, meta)

        # Step 8: Add visualization material
        setup_terrain_material(terrain, heightmap_img, meta)

        # Step 9: Add reference objects
        add_reference_grid(meta)
        add_scale_markers(meta)

        # Step 10: Set up camera
        setup_camera(meta)

        # Step 11: Add lighting
        add_sun_light(meta)

        # Step 12: Configure viewport
        setup_viewport(terrain)

        # Step 13: Set Blender scene units to metric
        context.scene.unit_settings.system = 'METRIC'
        context.scene.unit_settings.scale_length = 1.0
        context.scene.unit_settings.length_unit = 'METERS'

        print(f"\n{'='*60}")
        print(f"  TERRAIN IMPORT COMPLETE")
        print(f"  Dimensions: {meta['real_width_m']:.0f}m x {meta['real_height_m']:.0f}m")
        print(f"  Elevation range: {meta['elevation_max'] - meta['elevation_min']:.1f}m")
        print(f"  Vertices: {len(terrain.data.vertices):,}")
        print(f"{'='*60}\n")

        self.report({'INFO'}, f"Terrain imported: {meta['real_width_m']:.0f}x{meta['real_height_m']:.0f}m")
        return {'FINISHED'}

    def invoke(self, context, event):
        # Open file browser dialog
        context.window_manager.fileselect_add(self)
        return {'RUNNING_MODAL'}


