def menu_func_import(self, context):
    self.layout.operator(IMPORT_OT_heightmap.bl_idname, text="Heightmap DEM (.png)")


def register():
    bpy.utils.register_class(IMPORT_OT_heightmap)
    bpy.types.TOPBAR_MT_file_import.append(menu_func_import)


def unregister():
    bpy.utils.unregister_class(IMPORT_OT_heightmap)
    bpy.types.TOPBAR_MT_file_import.remove(menu_func_import)


# ---------------------------------------------------------------------------
# Main — when run directly from Blender's text editor
# ---------------------------------------------------------------------------

if __name__ == "__main__":
    register()

    # Check if a filepath was passed as argument (e.g., from command line)
    # Usage: blender --python import_dem.py -- /path/to/heightmap.png
    argv = sys.argv
    if "--" in argv:
        custom_args = argv[argv.index("--") + 1:]
        if custom_args:
            filepath = custom_args[0]
            if os.path.exists(filepath):
                print(f"[import_dem] Using command-line filepath: {filepath}")
                bpy.ops.import_scene.heightmap_dem(filepath=filepath)
            else:
                print(f"[import_dem] File not found: {filepath}")
                print("[import_dem] Opening file dialog instead...")
                bpy.ops.import_scene.heightmap_dem('INVOKE_DEFAULT')
        else:
            bpy.ops.import_scene.heightmap_dem('INVOKE_DEFAULT')
    else:
        # No command line args — open file dialog
        bpy.ops.import_scene.heightmap_dem('INVOKE_DEFAULT')
